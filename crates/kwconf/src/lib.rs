fn build_command(spec: &ConfigSpec, color: ColorChoice) -> Command {
    build_command_with_name(spec, spec.name, color)
}

fn build_command_with_name(spec: &ConfigSpec, name: &str, color: ColorChoice) -> Command {
    let mut cmd = Command::new(name.to_string())
        .styles(kwconf_help_styles())
        .color(color)
        .arg_required_else_help(false)
        .arg(
            Arg::new("config")
                .long("config")
                .value_name("PATH")
                .help("Read TOML, JSON, YAML, or YML config."),
        )
        .arg(
            Arg::new("generate-completion")
                .long("generate-completion")
                .value_name("SHELL")
                .value_parser(clap::value_parser!(Shell))
                .help("Generate a shell completion script."),
        )
        .arg(
            Arg::new("color")
                .long("color")
                .value_name("WHEN")
                .value_parser(["auto", "always", "never"])
                .help("Control help color: auto, always, or never."),
        );

    if let Some(about) = spec.about {
        cmd = cmd.about(about);
    }

    let mut paths = Vec::new();
    collect_named_field_paths(spec, Vec::new(), &mut paths);
    for (path_name, field) in paths {
        let mut arg = Arg::new(path_name.clone())
            .long(path_name.replace('_', "-"))
            .value_name("VALUE")
            .action(ArgAction::Set)
            .help(field_help(field));

        for alias in field.aliases {
            let alias_path = replace_leaf_name(&path_name, alias);
            arg = arg.visible_alias(alias_path.replace('_', "-"));
        }

        if !field.choices.is_empty() {
            arg = arg.value_parser(clap::builder::PossibleValuesParser::new(field.choices.iter().copied()));
        }

        cmd = cmd.arg(arg);
    }

    cmd
}

fn collect_named_field_paths<'a>(spec: &'a ConfigSpec, prefix: Vec<String>, out: &mut Vec<(String, &'a FieldInfo)>) {
    for field in spec.fields {
        let mut path = prefix.clone();
        path.push(field.name.to_string());
        match field.kind {
            FieldKind::Value => out.push((path.join("."), field)),
            FieldKind::Subconfig(child_spec) => collect_named_field_paths(child_spec, path, out),
        }
    }
}

fn replace_leaf_name(path: &str, leaf: &str) -> String {
    if let Some((head, _)) = path.rsplit_once('.') {
        format!("{head}.{leaf}")
    } else {
        leaf.to_string()
    }
}

fn field_help(field: &FieldInfo) -> String {
    let mut parts = Vec::new();
    if let Some(help) = field.help {
        parts.push(help.to_string());
    }
    if field.parser != Parser::Auto {
        parts.push(format!("parser={}", field.parser.name()));
    }
    if let Some(env) = field.env {
        parts.push(format!("env={env}"));
    }
    if !field.choices.is_empty() {
        parts.push(format!("choices={}", field.choices.join("|")));
    }
    parts.join(" ")
}

/// Resolved modal selection used by derive-generated code.
#[doc(hidden)]
pub struct ModalSelection {
    pub variant: &'static str,
    pub sources: Sources,
}

/// Resolve modal sources into one variant and child sources.
#[doc(hidden)]
pub fn resolve_modal_selection(spec: &'static ModalSpec, sources: Sources) -> Result<ModalSelection> {
    let parsed = parse_modal_argv(spec, &sources.args)?;

    if parsed.help_requested {
        let color = parsed.color_choice.unwrap_or(ColorChoice::Auto);
        return Err(Error::HelpRequested {
            text: render_modal_help_with_color(spec, color),
            color,
        });
    }

    if let Some(shell) = parsed.completion_shell {
        return Err(Error::CompletionRequested(render_modal_completion(spec, shell, spec.name)));
    }

    let config_path = parsed.config_path.clone().or_else(|| sources.config_path.clone());
    let config_value = if let Some(value) = sources.config_value.clone() {
        Some(value)
    } else if let Some(path) = config_path.as_ref() {
        Some(read_config_file(path)?)
    } else {
        None
    };

    let variant_name = if let Some(name) = parsed.variant_name.as_deref() {
        canonical_modal_variant(spec, name)?.name
    } else if let Some(name) = modal_variant_from_config(&config_value) {
        canonical_modal_variant(spec, &name)?.name
    } else if let Some(name) = spec.default_variant {
        canonical_modal_variant(spec, name)?.name
    } else {
        spec.variants
            .first()
            .map(|variant| variant.name)
            .ok_or_else(|| Error::Message("modal config has no variants".to_string()))?
    };

    let mut child_sources = sources.clone();
    child_sources.args = parsed.child_args.unwrap_or_else(|| vec![OsString::from(variant_name)]);
    child_sources.config_path = None;
    child_sources.config_value = config_value.and_then(|value| modal_child_config_value(spec, variant_name, value));

    Ok(ModalSelection {
        variant: variant_name,
        sources: child_sources,
    })
}

#[derive(Debug, Default)]
struct ParsedModalArgv {
    variant_name: Option<String>,
    child_args: Option<Vec<OsString>>,
    config_path: Option<PathBuf>,
    completion_shell: Option<Shell>,
    color_choice: Option<ColorChoice>,
    help_requested: bool,
}

fn parse_modal_argv(spec: &ModalSpec, args: &[OsString]) -> Result<ParsedModalArgv> {
    let mut parsed = ParsedModalArgv::default();
    let program = args
        .first()
        .cloned()
        .unwrap_or_else(|| OsString::from(spec.name));
    let mut iter = args.iter().skip(1).peekable();

    while let Some(raw) = iter.next() {
        let arg = raw.to_str().ok_or_else(|| Error::Message("argv contains non-UTF-8 text".to_string()))?;

        if arg == "--help" || arg == "-h" {
            parsed.help_requested = true;
            continue;
        }

        if arg == "--" {
            break;
        }

        if arg.starts_with("--") {
            let body = &arg[2..];
            let (key, value) = if let Some((key, value)) = body.split_once('=') {
                (key.to_string(), value.to_string())
            } else {
                let key = body.to_string();
                let value = match iter.peek() {
                    Some(next) if !next.to_string_lossy().starts_with("--") => {
                        iter.next()
                            .and_then(|v| v.to_str().map(ToOwned::to_owned))
                            .ok_or_else(|| Error::Message("argv contains non-UTF-8 text".to_string()))?
                    }
                    _ => "true".to_string(),
                };
                (key, value)
            };
            let key = normalize_key(&key);
            if key == "config" {
                if value == "true" {
                    return Err(Error::MissingValue("--config".to_string()));
                }
                parsed.config_path = Some(PathBuf::from(value));
                continue;
            }
            if key == "generate_completion" {
                if value == "true" {
                    return Err(Error::MissingValue("--generate-completion".to_string()));
                }
                parsed.completion_shell = Some(parse_shell(&value)?);
                continue;
            }
            if key == "color" {
                if value == "true" {
                    return Err(Error::MissingValue("--color".to_string()));
                }
                parsed.color_choice = Some(parse_color_choice(&value)?);
                continue;
            }
            return Err(Error::UnknownArgument(format!("--{key}")));
        }

        let variant = canonical_modal_variant(spec, arg)?.name.to_string();
        let mut child_args = Vec::new();
        child_args.push(OsString::from(format!("{} {}", program.to_string_lossy(), variant)));
        for item in iter {
            child_args.push(item.clone());
        }
        parsed.variant_name = Some(variant);
        parsed.child_args = Some(child_args);
        return Ok(parsed);
    }

    Ok(parsed)
}

fn canonical_modal_variant<'a>(spec: &'a ModalSpec, name: &str) -> Result<&'a ModalVariantInfo> {
    let key = normalize_key(name);
    spec.variants
        .iter()
        .find(|variant| {
            normalize_key(variant.name) == key || variant.aliases.iter().any(|alias| normalize_key(alias) == key)
        })
        .ok_or_else(|| Error::InvalidModalVariant(name.to_string()))
}

fn modal_variant_from_config(config_value: &Option<Value>) -> Option<String> {
    let Some(Value::Object(map)) = config_value else {
        return None;
    };
    map.get("command")
        .or_else(|| map.get("mode"))
        .and_then(Value::as_str)
        .map(ToOwned::to_owned)
}

fn modal_child_config_value(spec: &ModalSpec, variant_name: &str, value: Value) -> Option<Value> {
    let Value::Object(mut map) = value else {
        return None;
    };

    if let Some(value) = map.remove(variant_name) {
        return Some(value);
    }

    for variant in spec.variants {
        if let Some(value) = map.remove(variant.name) {
            if normalize_key(variant.name) == normalize_key(variant_name) {
                return Some(value);
            }
        }
        for alias in variant.aliases {
            if let Some(value) = map.remove(*alias) {
                if normalize_key(alias) == normalize_key(variant_name) {
                    return Some(value);
                }
            }
        }
    }

    map.remove("command");
    map.remove("mode");
    for variant in spec.variants {
        map.remove(variant.name);
        for alias in variant.aliases {
            map.remove(*alias);
        }
    }

    if map.is_empty() {
        None
    } else {
        Some(Value::Object(map))
    }
}

