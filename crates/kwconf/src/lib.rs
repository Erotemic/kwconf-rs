fn build_modal_command(spec: &ModalSpec, color: ColorChoice) -> Command {
    let mut cmd = Command::new(spec.name)
        .styles(kwconf_help_styles())
        .color(color)
        .arg_required_else_help(false)
        .subcommand_required(false)
        .arg(
            Arg::new("config")
                .long("config")
                .value_name("PATH")
                .help("Read TOML, JSON, YAML, or YML config."),
        )
        .arg(
            Arg::new("generate-completion")
                .long("generate-completion")
                .value_name("SHELL")
                .value_parser(clap::value_parser!(Shell))
                .help("Generate a shell completion script."),
        )
        .arg(
            Arg::new("color")
                .long("color")
                .value_name("WHEN")
                .value_parser(["auto", "always", "never"])
                .help("Control help color: auto, always, or never."),
        );

    if let Some(about) = spec.about {
        cmd = cmd.about(about);
    }

    for variant in spec.variants {
        let mut sub = build_command_with_name(variant.config_spec, variant.name, color);
        if let Some(help) = variant.help {
            sub = sub.about(help);
        }
        for alias in variant.aliases {
            sub = sub.visible_alias(alias.replace('_', "-"));
        }
        cmd = cmd.subcommand(sub);
    }

    cmd
}

